num = int(input("Enter num : "))

if num > 0:
    print(" Number is positive ")
elif num < 0:
    print(" Number is negative ")
else:
    print(" Number is zero ")
#even/odd check
if num%2 == 0:
    print(" Number is even ")
else:
    print(" Number is odd ")

